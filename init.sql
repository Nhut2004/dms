-- =====================================================================
-- V. TRIGGER CAP NHAT ngay_cap_nhat
-- =====================================================================

CREATE OR REPLACE FUNCTION fn_cap_nhat_thoi_gian()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.ngay_cap_nhat := CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_tai_khoan_cap_nhat
BEFORE UPDATE ON tai_khoan
FOR EACH ROW
EXECUTE FUNCTION fn_cap_nhat_thoi_gian();

CREATE TRIGGER trg_quyet_dinh_cap_nhat
BEFORE UPDATE ON quyet_dinh
FOR EACH ROW
EXECUTE FUNCTION fn_cap_nhat_thoi_gian();

