CREATE TABLE tai_khoan (
    id                  integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    can_bo_id           integer UNIQUE,
    ten_dang_nhap       varchar(100) NOT NULL UNIQUE,
    mat_khau_hash       varchar(255) NOT NULL,
    trang_thai          varchar(20) NOT NULL DEFAULT 'ACTIVE',
    ngay_tao            timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ngay_cap_nhat       timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_tai_khoan_can_bo
        FOREIGN KEY (can_bo_id)
        REFERENCES can_bo(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_tai_khoan_trang_thai
        CHECK (trang_thai IN ('ACTIVE', 'LOCKED', 'INACTIVE'))
);

CREATE TABLE vai_tro (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_vai_tro      varchar(50) NOT NULL UNIQUE,
    ten_vai_tro     varchar(100) NOT NULL,
    mo_ta           varchar(500)
);

CREATE TABLE quyen (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_quyen        varchar(100) NOT NULL UNIQUE,
    ten_quyen       varchar(150) NOT NULL,
    module          varchar(50) NOT NULL,
    hanh_dong       varchar(30) NOT NULL
);

CREATE TABLE tai_khoan_vai_tro (
    tai_khoan_id    integer NOT NULL,
    vai_tro_id      integer NOT NULL,

    PRIMARY KEY (tai_khoan_id, vai_tro_id),

    CONSTRAINT fk_tkvt_tai_khoan
        FOREIGN KEY (tai_khoan_id)
        REFERENCES tai_khoan(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_tkvt_vai_tro
        FOREIGN KEY (vai_tro_id)
        REFERENCES vai_tro(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE vai_tro_quyen (
    vai_tro_id      integer NOT NULL,
    quyen_id        integer NOT NULL,

    PRIMARY KEY (vai_tro_id, quyen_id),

    CONSTRAINT fk_vtq_vai_tro
        FOREIGN KEY (vai_tro_id)
        REFERENCES vai_tro(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_vtq_quyen
        FOREIGN KEY (quyen_id)
        REFERENCES quyen(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

