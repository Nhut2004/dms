-- I. CAC BANG GOC TU ERD BAN DAU
-- =====================================================================

CREATE TABLE danh_muc_loai_vb (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ten_loai_vb     varchar(100) NOT NULL UNIQUE,
    mo_ta           text
);

CREATE TABLE co_quan_to_chuc (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ten_co_quan     varchar(255) NOT NULL,
    organ_id        varchar(13) NOT NULL UNIQUE,
    dia_chi         varchar(500)
);

CREATE TABLE can_bo (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ho_ten          varchar(100) NOT NULL,
    chuc_vu         varchar(100),
    co_quan_id      integer NOT NULL,

    CONSTRAINT fk_can_bo_co_quan
        FOREIGN KEY (co_quan_id)
        REFERENCES co_quan_to_chuc(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE vi_tri_luu_tru (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    toa_nha         varchar(255),
    phong           varchar(100),
    ke_tu           varchar(100),
    ngan_tang       varchar(100),
    so_hop          varchar(100),
    ghi_chu         text
);

CREATE TABLE ho_so (
    ma_ho_so            varchar(50) PRIMARY KEY,
    tieu_de_ho_so       varchar(500) NOT NULL,
    thoi_han_bao_quan   varchar(50),
    che_do_su_dung      varchar(50),
    ngay_bat_dau        date,
    ngay_ket_thuc       date,
    so_luong_trang      integer DEFAULT 0,
    so_luong_van_ban    integer DEFAULT 0,
    nguoi_lap           varchar(100),
    ngon_ngu            varchar(50),
    ghi_chu             text,
    vi_tri_id           integer,

    CONSTRAINT fk_ho_so_vi_tri
        FOREIGN KEY (vi_tri_id)
        REFERENCES vi_tri_luu_tru(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_ho_so_ngay
        CHECK (
            ngay_ket_thuc IS NULL
            OR ngay_bat_dau IS NULL
            OR ngay_ket_thuc >= ngay_bat_dau
        ),

    CONSTRAINT chk_ho_so_so_luong_trang
        CHECK (so_luong_trang IS NULL OR so_luong_trang >= 0),

    CONSTRAINT chk_ho_so_so_luong_van_ban
        CHECK (so_luong_van_ban IS NULL OR so_luong_van_ban >= 0)
);

CREATE TABLE van_ban_den (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    so_den                  integer NOT NULL,
    ky_hieu                 varchar(100),
    ngay_den                date NOT NULL,
    ngay_ban_hanh           date,
    co_quan_ban_hanh_id     integer,
    ma_loai_vb_id           integer NOT NULL,
    trich_yeu               text NOT NULL,
    ngon_ngu                varchar(30),
    so_trang                integer,
    ho_ten_nguoi_ky         varchar(50),
    chuc_vu_nguoi_ky        varchar(100),
    linh_vuc                text,
    do_khan                 integer,
    don_vi_nhan             text,
    han_giai_quyet          date,
    y_kien_chi_dao          text,
    stt_trong_ho_so         integer,
    ma_ho_so                varchar(50),

    CONSTRAINT fk_van_ban_den_co_quan
        FOREIGN KEY (co_quan_ban_hanh_id)
        REFERENCES co_quan_to_chuc(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_van_ban_den_loai_vb
        FOREIGN KEY (ma_loai_vb_id)
        REFERENCES danh_muc_loai_vb(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_van_ban_den_ho_so
        FOREIGN KEY (ma_ho_so)
        REFERENCES ho_so(ma_ho_so)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_van_ban_den_so_den
        CHECK (so_den > 0),

    CONSTRAINT chk_van_ban_den_so_trang
        CHECK (so_trang IS NULL OR so_trang >= 0),

    CONSTRAINT chk_van_ban_den_do_khan
        CHECK (do_khan IS NULL OR do_khan BETWEEN 1 AND 5)
);

CREATE TABLE van_ban_di (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    so_ky_hieu              varchar(50),
    ngay_ban_hanh           date,
    trich_yeu               text NOT NULL,
    don_vi_soan_thao_id     integer NOT NULL,
    ma_loai_vb_id           integer NOT NULL,
    ngon_ngu                varchar(30),
    so_trang                integer,
    ghi_chu                 text,
    nguoi_ky_id             integer,
    chuc_vu_nguoi_ky        varchar(100),
    noi_nhan                varchar(100),
    muc_do_khan             integer,
    han_tra_loi             date,
    stt_trong_ho_so         integer,
    ma_ho_so                varchar(50),

    CONSTRAINT fk_van_ban_di_don_vi_soan_thao
        FOREIGN KEY (don_vi_soan_thao_id)
        REFERENCES co_quan_to_chuc(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_van_ban_di_loai_vb
        FOREIGN KEY (ma_loai_vb_id)
        REFERENCES danh_muc_loai_vb(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_van_ban_di_nguoi_ky
        FOREIGN KEY (nguoi_ky_id)
        REFERENCES can_bo(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT fk_van_ban_di_ho_so
        FOREIGN KEY (ma_ho_so)
        REFERENCES ho_so(ma_ho_so)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_van_ban_di_so_trang
        CHECK (so_trang IS NULL OR so_trang >= 0),

    CONSTRAINT chk_van_ban_di_muc_do_khan
        CHECK (muc_do_khan IS NULL OR muc_do_khan BETWEEN 1 AND 5)
);

CREATE TABLE noi_nhan_van_ban (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    van_ban_di_id           integer NOT NULL,
    co_quan_nhan_id         integer NOT NULL,
    phuong_thuc_gui         varchar(100),

    CONSTRAINT fk_noi_nhan_van_ban_di
        FOREIGN KEY (van_ban_di_id)
        REFERENCES van_ban_di(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_noi_nhan_co_quan
        FOREIGN KEY (co_quan_nhan_id)
        REFERENCES co_quan_to_chuc(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT uk_noi_nhan_van_ban
        UNIQUE (van_ban_di_id, co_quan_nhan_id)
);

CREATE TABLE file_dinh_kem (
    id              integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    loai_van_ban    varchar(20) NOT NULL,
    van_ban_id      integer NOT NULL,
    ten_file        varchar(255) NOT NULL,
    duong_dan       varchar(500) NOT NULL,
    dinh_dang       varchar(10),
    dung_luong      double precision,
    ngay_tao        timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT chk_file_loai_van_ban
        CHECK (loai_van_ban IN ('VAN_BAN_DEN', 'VAN_BAN_DI')),

    CONSTRAINT chk_file_dung_luong
        CHECK (dung_luong IS NULL OR dung_luong >= 0)
);

