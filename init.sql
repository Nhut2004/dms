CREATE TABLE danh_muc_loai_quyet_dinh (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_loai_quyet_dinh      varchar(50) NOT NULL UNIQUE,
    ten_loai_quyet_dinh     varchar(150) NOT NULL,
    mo_ta                   text
);

CREATE TABLE quyet_dinh (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    van_ban_di_id           integer NOT NULL UNIQUE,
    loai_quyet_dinh_id      integer NOT NULL,
    ngay_hieu_luc           date,
    trang_thai              varchar(20) NOT NULL DEFAULT 'DRAFT',
    nguoi_tao_id            integer NOT NULL,
    nguoi_duyet_id          integer,
    ngay_trinh_duyet        timestamp without time zone,
    ngay_duyet              timestamp without time zone,
    ly_do_tu_choi           text,
    ngay_tao                timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ngay_cap_nhat           timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_quyet_dinh_van_ban_di
        FOREIGN KEY (van_ban_di_id)
        REFERENCES van_ban_di(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_quyet_dinh_loai
        FOREIGN KEY (loai_quyet_dinh_id)
        REFERENCES danh_muc_loai_quyet_dinh(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_quyet_dinh_nguoi_tao
        FOREIGN KEY (nguoi_tao_id)
        REFERENCES tai_khoan(id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,

    CONSTRAINT fk_quyet_dinh_nguoi_duyet
        FOREIGN KEY (nguoi_duyet_id)
        REFERENCES tai_khoan(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,

    CONSTRAINT chk_quyet_dinh_trang_thai
        CHECK (
            trang_thai IN (
                'DRAFT',
                'SUBMITTED',
                'APPROVED',
                'REJECTED',
                'ISSUED',
                'CANCELLED'
            )
        )
);

CREATE TABLE lich_su_quyet_dinh (
    id                      integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    quyet_dinh_id           integer NOT NULL,
    trang_thai_cu           varchar(20),
    trang_thai_moi          varchar(20) NOT NULL,
    hanh_dong               varchar(50) NOT NULL,
    noi_dung_y_kien         text,
    nguoi_thuc_hien_id      integer,
    thoi_gian               timestamp without time zone NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT fk_lich_su_quyet_dinh
        FOREIGN KEY (quyet_dinh_id)
        REFERENCES quyet_dinh(id)
        ON UPDATE CASCADE
        ON DELETE CASCADE,

    CONSTRAINT fk_lich_su_qd_nguoi_thuc_hien
        FOREIGN KEY (nguoi_thuc_hien_id)
        REFERENCES tai_khoan(id)
        ON UPDATE CASCADE
        ON DELETE SET NULL
);

